Font::Font(const Common::String &filename, Common::SeekableReadStream *data) :
		PoolObject<Font, MKTAG('F', 'O', 'N', 'T')>() {
	load(filename, data);
}

Font::Font() :
		PoolObject<Font, MKTAG('F', 'O', 'N', 'T')>() {
	_charIndex = NULL;
	_font = NULL;
}

Font::~Font() {
	if (_charIndex) {
		delete[] _charIndex;
		delete[] _charHeaders;
		delete[] _fontData;
	}
	delete _font;
}

void Font::load(const Common::String &filename, Common::SeekableReadStream *data) {
	_filename = filename;
	_numChars = data->readUint32LE();
	_dataSize = data->readUint32LE();
	_height = data->readUint32LE();
	_baseOffsetY = data->readUint32LE();
	data->seek(24, SEEK_SET);
	_firstChar = data->readUint32LE();
	_lastChar = data->readUint32LE();
	int8 available_height = _height - _baseOffsetY;

	// Read character indexes - are the key/value reversed?
	_charIndex = new uint16[_numChars];
	if (!_charIndex)
		error("Could not load font %s. Out of memory", _filename.c_str());
	for (uint i = 0; i < _numChars; ++i)
		_charIndex[i] = data->readUint16LE();

	// Read character headers
	_charHeaders = new CharHeader[_numChars];
	if (!_charHeaders)
		error("Could not load font %s. Out of memory", _filename.c_str());
	for (uint i = 0; i < _numChars; ++i) {
		_charHeaders[i].offset = data->readUint32LE();
		_charHeaders[i].width = data->readSByte();
		_charHeaders[i].startingCol = data->readSByte();
		_charHeaders[i].startingLine = data->readSByte();
		data->seek(1, SEEK_CUR);
		_charHeaders[i].dataWidth = data->readUint32LE();
		_charHeaders[i].dataHeight = data->readUint32LE();
		int8 overflow = (_charHeaders[i].dataHeight + _charHeaders[i].startingLine) - available_height;
		if (overflow > 0) {
			warning("Font %s, char 0x%02x exceeds font height by %d, increasing font height", _filename.c_str(), i, overflow);
			available_height += overflow;
			_height += overflow;
		}
	}
	// Read font data
	_fontData = new byte[_dataSize];
	if (!_fontData)
		error("Could not load font %s. Out of memory", _filename.c_str());

	data->read(_fontData, _dataSize);


	// Create the texture
	const byte *bitmapData = getFontData();
	uint dataSize = getDataSize();

	uint8 bpp = 4;
	uint8 charsWide = 16;
	uint8 charsHigh = 16;

	byte *texDataPtr = new byte[dataSize * bpp];
	byte *dat = texDataPtr;

	for (uint i = 0; i < dataSize; i++, texDataPtr += bpp, bitmapData++) {
		byte pixel = *bitmapData;
		if (pixel == 0x00) {
			texDataPtr[0] = texDataPtr[1] = texDataPtr[2] = texDataPtr[3] = 0;
		} else if (pixel == 0x80) {
			texDataPtr[0] = texDataPtr[1] = texDataPtr[2] = 0;
			texDataPtr[3] = 255;
		} else if (pixel == 0xFF) {
			texDataPtr[0] = texDataPtr[1] = texDataPtr[2] = texDataPtr[3] = 255;
		}
	}
	_quadSize = 0;
	for (int i = 0; i < 256; ++i) {
		int width = getCharDataWidth(i), height = getCharDataHeight(i);
		int m = MAX(width, height);
		if (m > _quadSize)
			_quadSize = m;
	}
	assert(_quadSize < 64);
	if (_quadSize < 8)
		_quadSize = 8;
	if (_quadSize < 16)
		_quadSize = 16;
	else if (_quadSize < 32)
		_quadSize = 32;
	else if (_quadSize < 64)
		_quadSize = 64;

	uint arraySize = _quadSize * _quadSize * bpp * charsWide * charsHigh;
	byte *temp = new byte[arraySize];
	if (!temp)
		error("Could not allocate %d bytes", arraySize);

	memset(temp, 0, arraySize);

	for (int i = 0, row = 0; i < 256; ++i) {
		int width = getCharDataWidth(i), height = getCharDataHeight(i);
		int32 d = getCharOffset(i);
		for (int x = 0; x < height; ++x) {
			// a is the offset to get to the correct row.
			// b is the offset to get to the correct line in the character.
			// c is the offset of the character from the start of the row.
			uint a = row * _quadSize * _quadSize * bpp * charsHigh;
			uint b = x * _quadSize * charsWide * bpp;
			uint c = 0;
			if (i != 0)
				c = ((i - 1) % 16) * _quadSize * bpp;

			uint pos = a + b + c;
			uint pos2 = d * bpp + x * width * bpp;
			assert(pos + width * bpp <= arraySize);
			assert(pos2 + width * bpp <= dataSize * bpp);
			memcpy(temp + pos, dat + pos2, width * bpp);
		}
		if (i != 0 && i % charsWide == 0)
			++row;
	}

	Graphics::PixelFormat f(4, 8, 8, 8, 8, 0, 8, 16, 24);
	_font = AGLMan.createFont(this, Graphics::PixelBuffer(f, temp), _quadSize * charsWide, _quadSize * charsHigh);
}

uint16 Font::getCharIndex(unsigned char c) const {
	uint16 c2 = uint16(c);

	// In order to ensure the correct character codes for
	// accented characters it is necessary to check the
	// requested code against the index of characters for
	// the font.  Previously, signed characters were
	// causing the problem but it might be possible for
	// an invalid character to be called for other reasons.
	//
	// Example: Without this fix when Manny greets Eva
	// for the first time and he says "Buenos Días" the
	// 'í' character will either show up as a different
	// character or it crashes the game.

	if (_charIndex[c2] == c2) {
		return c2;
	}

	for (uint i = 0; i < _numChars; ++i) {
		if (_charIndex[i] == c2)
			return i;
	}
	Debug::warning(Debug::Fonts, "The requsted character (code 0x%x) does not correspond to anything in the font data!", c2);
	// If we couldn't find the character then default to
	// the first character in the font so that something
	// gets loaded to prevent the game from crashing
	return 0;
}

int Font::getCharWidth(unsigned char c) const {
	return MAX((int)_charHeaders[getCharIndex(c)].width, getCharDataWidth(c));
}

int Font::getStringLength(const Common::String &text) const {
	int result = 0;
	for (uint32 i = 0; i < text.size(); ++i) {
		result += MAX(getCharDataWidth(text[i]), getCharWidth(text[i]));
	}
	return result;
}

Math::Rect2d Font::getCharTextureRect(unsigned char c) const {
	float width = 1 / 16.f;
	float cx = ((c - 1) % 16) / 16.0f;
	float cy = ((c - 1) / 16) / 16.0f;

	return Math::Rect2d(cx, cy, width, width);
}

Math::Rect2d Font::getCharQuadRect(unsigned char c) const {
	float y = getCharStartingLine(c) + getBaseOffsetY();
	float x = getCharStartingCol(c);

	return Math::Rect2d(x, y, _quadSize, _quadSize);
}

void Font::saveState(SaveGame *state) const {
	state->writeString(getFilename());
}

void Font::restoreState(SaveGame *state) {
	Common::String fname = state->readString();
	Common::SeekableReadStream *stream;

	stream = g_resourceloader->openNewStreamFile(fname.c_str(), true);
	load(fname, stream);
	delete stream;
}

